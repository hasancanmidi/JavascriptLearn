let sayi = 1;

do {
  console.log("Sayı: " + sayi);
  sayi++;
} while (sayi <= 5);


/*
let sayi = 1;: Bir sayı değişkeni sayi'yi 1 olarak başlatır.
do { ... }: Döngü gövdesi, koşul kontrol edilmeden önce en az bir kez çalışır.
console.log("Sayı: " + sayi);: Döngü her adımda sayi'yi konsola yazdırır.
sayi++;: Her adımda sayi değeri bir artar.
while (sayi <= 5);: Koşul ifadesi, sayi'nin 5 veya daha küçük olduğu sürece döngünün devam etmesini sağlar.
*/
